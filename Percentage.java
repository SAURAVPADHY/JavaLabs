public class Percentage {
    public static void main(String[] args){
        int math=95;
        int physics=98;
        int chemistry=88;
        int biology=78;
        int sanskrit=99;
        int odia=70;
        double Percentage =(math+physics+sanskrit+odia+chemistry+biology)/600d*100d;
        System.out.println("Percentage: " +Percentage+"%");
    }
    
}
